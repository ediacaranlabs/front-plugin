package br.com.uoutec.community.ediacaran.front.objects;

public class ObjectsManagerDriverException extends RuntimeException{

	private static final long serialVersionUID = -676360776297912075L;

	public ObjectsManagerDriverException() {
		super();
	}

	public ObjectsManagerDriverException(String message, Throwable cause) {
		super(message, cause);
	}

	public ObjectsManagerDriverException(String message) {
		super(message);
	}

	public ObjectsManagerDriverException(Throwable cause) {
		super(cause);
	}

}
