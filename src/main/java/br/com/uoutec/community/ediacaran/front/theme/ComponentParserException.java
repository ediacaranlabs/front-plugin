package br.com.uoutec.community.ediacaran.front.theme;

public class ComponentParserException extends ThemeException{

	private static final long serialVersionUID = 878447505020553269L;

	public ComponentParserException() {
		super();
	}

	public ComponentParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public ComponentParserException(String message) {
		super(message);
	}

	public ComponentParserException(Throwable cause) {
		super(cause);
	}

}
